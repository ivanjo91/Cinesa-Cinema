package modelo;

/**
 *
 * @author David
 */
public class Sala {
    
    private int idSala, filas, columnas;

    public Sala() {
    }

    public Sala(int idSala, int filas, int columnas) {
        this.idSala = idSala;
        this.filas = filas;
        this.columnas = columnas;
    }

    public int getIdSala() {
        return idSala;
    }

    public void setIdSala(int idSala) {
        this.idSala = idSala;
    }

    public int getFilas() {
        return filas;
    }

    public void setFilas(int filas) {
        this.filas = filas;
    }

    public int getColumnas() {
        return columnas;
    }

    public void setColumnas(int columnas) {
        this.columnas = columnas;
    }
    
    
}
